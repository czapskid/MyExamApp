    private void jLabel6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel6MouseClicked
        if(wybranyDzial == 0)
        {
            JOptionPane.showMessageDialog(null, "Wybierz najpierw dział.");
            return;
        }
        List<Zadanie> zl = dbAccess.wezZadania(5, wybranyDzial);   //  dobrane przypadkowo
        QuestionDialog qd = new QuestionDialog(this, true);
        qd.setTitle("Rozwiąż zadanie");
        qd.setLayout(new BorderLayout());
        qd.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        ZadaniaPane zp = new ZadaniaPane(zl, qd);
        qd.add(zp);
        qd.pack();
        qd.setLocationRelativeTo(null);
        Date dStart = new Date();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        String dsStart = sdf.format(dStart);
        qd.setVisible(true);
        sprawdzZapiszStatystyke(zl, dsStart);
        wybranyDzial = 0;

    }//GEN-LAST:event_jLabel6MouseClicked

