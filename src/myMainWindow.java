    private void sprawdzZapiszStatystyke(List<Zadanie> zl, String dsStart)
    {
        for (Zadanie zadanie : zl) {
            int id_zadania = zadanie.getId_zadania();
            int odpowiedz = wybraneOdpowiedzi.get(id_zadania);
            boolean poprawne = (zadanie.getPoprawna() == odpowiedz);
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
            String dsStop = sdf.format(new Date());
            dbAccess.zapisz_statystyke(jLabelUsername.getText(), id_zadania, poprawne, dsStart, dsStop);
        }
    }

